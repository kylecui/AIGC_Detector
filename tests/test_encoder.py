class TestTextClassificationDataset:
    def test_load_from_jsonl(self, tmp_path):
        from aigc_detector.training.trainer import TextClassificationDataset

        records = [
            {"text": "Human written text about science.", "label": "human"},
            {"text": "AI generated content about technology.", "label": "ai"},
            {"text": "Another human text about history.", "label": "human"},
            {"text": "Mixed content example.", "label": "mixed"},  # should be skipped
        ]
        path = tmp_path / "data.jsonl"
        with open(path, "w") as f:
            for r in records:
                f.write(json.dumps(r) + "\n")

        mock_tokenizer = MagicMock()
        ds = TextClassificationDataset(path, mock_tokenizer)
        assert len(ds) == 3  # "mixed" is skipped
        assert ds.labels == [0, 1, 0]  # human=0, ai=1

    def test_file_not_found(self, tmp_path):
        from aigc_detector.training.trainer import TextClassificationDataset

        mock_tokenizer = MagicMock()
        with pytest.raises(FileNotFoundError, match="Dataset file not found"):
            TextClassificationDataset(tmp_path / "nonexistent.jsonl", mock_tokenizer)

    def test_getitem_returns_dict(self, tmp_path):
        from aigc_detector.training.trainer import TextClassificationDataset

        records = [
            {"text": "Test text one.", "label": "human"},
            {"text": "Test text two.", "label": "ai"},
        ]
        path = tmp_path / "data.jsonl"
        with open(path, "w") as f:
            for r in records:
                f.write(json.dumps(r) + "\n")

        mock_tokenizer = MagicMock()
        mock_tokenizer.return_value = {
            "input_ids": torch.ones(1, 512, dtype=torch.long),
            "attention_mask": torch.ones(1, 512, dtype=torch.long),
        }

        ds = TextClassificationDataset(path, mock_tokenizer, max_length=512)
        item = ds[0]
        assert "input_ids" in item
        assert "attention_mask" in item
        assert "labels" in item
        assert item["labels"].dtype == torch.long
        assert item["labels"].item() == 0  # human

    def test_custom_keys(self, tmp_path):
        from aigc_detector.training.trainer import TextClassificationDataset

        records = [
            {"content": "Some text.", "tag": "human"},
            {"content": "More text.", "tag": "ai"},
        ]
        path = tmp_path / "custom.jsonl"
        with open(path, "w") as f:
            for r in records:
                f.write(json.dumps(r) + "\n")

        mock_tokenizer = MagicMock()
        ds = TextClassificationDataset(path, mock_tokenizer, text_key="content", label_key="tag")
        assert len(ds) == 2
        assert ds.texts == ["Some text.", "More text."]


