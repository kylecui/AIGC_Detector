class TestEncoderResult:
    def test_basic_creation(self):
        from aigc_detector.detection.encoder import EncoderResult

        r = EncoderResult(label="ai", p_ai=0.92, confidence=0.92, model_name="test-model")
        assert r.label == "ai"
        assert r.p_ai == 0.92
        assert r.confidence == 0.92
        assert r.model_name == "test-model"

    def test_to_dict(self):
        from aigc_detector.detection.encoder import EncoderResult

        r = EncoderResult(label="human", p_ai=0.3, confidence=0.7, model_name="deberta-v3")
        d = r.to_dict()
        assert d["label"] == "human"
        assert d["p_ai"] == 0.3
        assert d["confidence"] == 0.7
        assert d["model_name"] == "deberta-v3"
        assert len(d) == 4

    def test_human_label(self):
        from aigc_detector.detection.encoder import EncoderResult

        r = EncoderResult(label="human", p_ai=0.1, confidence=0.9, model_name="m")
        assert r.label == "human"
        assert r.confidence == 0.9


