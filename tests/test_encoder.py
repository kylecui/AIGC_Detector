class TestComputeMetrics:
    def test_perfect_predictions(self):
        from aigc_detector.training.trainer import compute_metrics

        logits = np.array([[2.0, 0.1], [0.1, 2.0], [2.0, 0.1], [0.1, 2.0]])
        labels = np.array([0, 1, 0, 1])
        result = compute_metrics((logits, labels))
        assert result["accuracy"] == 1.0
        assert result["f1"] == 1.0

    def test_mixed_predictions(self):
        from aigc_detector.training.trainer import compute_metrics

        logits = np.array([[2.0, 0.1], [2.0, 0.1], [0.1, 2.0], [0.1, 2.0]])
        labels = np.array([0, 1, 0, 1])
        result = compute_metrics((logits, labels))
        assert result["accuracy"] == 0.5

    def test_all_same_class(self):
        from aigc_detector.training.trainer import compute_metrics

        logits = np.array([[2.0, 0.1], [2.0, 0.1]])
        labels = np.array([0, 0])
        result = compute_metrics((logits, labels))
        assert result["accuracy"] == 1.0
        assert result["f1"] == 0.0  # no positive class predicted


