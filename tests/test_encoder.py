    def test_predict_proba(self):
        from aigc_detector.detection.encoder import EncoderClassifier

        clf = EncoderClassifier(device="cpu")

        mock_model = MagicMock()
        mock_model.return_value = MagicMock(logits=torch.tensor([[0.5, 1.5]]))

        mock_tokenizer = MagicMock()
        mock_tokenizer.return_value = {
            "input_ids": torch.tensor([[1, 2]]),
            "attention_mask": torch.tensor([[1, 1]]),
        }

        clf._model = mock_model
        clf._tokenizer = mock_tokenizer

        p = clf.predict_proba("test")
        assert isinstance(p, float)
        assert 0.0 <= p <= 1.0

    def test_predict_with_threshold(self):
        from aigc_detector.detection.encoder import EncoderClassifier

        clf = EncoderClassifier(device="cpu")

        mock_model = MagicMock()
        # p_ai will be softmax([0.5, 1.0])[1] ≈ 0.62
        mock_model.return_value = MagicMock(logits=torch.tensor([[0.5, 1.0]]))

        mock_tokenizer = MagicMock()
        mock_tokenizer.return_value = {
            "input_ids": torch.tensor([[1, 2]]),
            "attention_mask": torch.tensor([[1, 1]]),
        }

        clf._model = mock_model
        clf._tokenizer = mock_tokenizer

        # With default threshold 0.5, should be "ai"
        r1 = clf.predict_with_threshold("test", threshold=0.5)
        assert r1.label == "ai"

        # With high threshold 0.9, should be "human"
        r2 = clf.predict_with_threshold("test", threshold=0.9)
        assert r2.label == "human"

    def test_unload(self):
        from aigc_detector.detection.encoder import EncoderClassifier

        clf = EncoderClassifier(device="cpu")
        clf._model = MagicMock()
        clf._tokenizer = MagicMock()
        assert clf.is_loaded
        clf.unload()
        assert not clf.is_loaded
        assert clf._model is None
        assert clf._tokenizer is None

    def test_load_idempotent(self):
        """Calling load() when already loaded should be a no-op."""
        from aigc_detector.detection.encoder import EncoderClassifier

        clf = EncoderClassifier(device="cpu")
        mock_model = MagicMock()
        clf._model = mock_model
        clf._tokenizer = MagicMock()

        # load() should not replace the existing model
        clf.load()
        assert clf._model is mock_model


